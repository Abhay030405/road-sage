def _build_parser() -> argparse.ArgumentParser:
    p = argparse.ArgumentParser(
        description=(
            "Evaluate the UFLD v2 lane detector on held-out MNNIT images."
        ),
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    p.add_argument(
        "--source",
        default="rgb",
        metavar="DIR",
        help="Directory containing rgb_image_*.png frames.",
    )
    p.add_argument(
        "--config",
        default="configs/lane_detection.yaml",
        metavar="YAML",
        help="Path to lane_detection.yaml.",
    )
    p.add_argument(
        "--max-images",
        type=int,
        default=None,
        metavar="N",
        help="Cap total images before the 90/10 split (smoke-test mode).",
    )
    p.add_argument(
        "--save-viz",
        action="store_true",
        help="Save annotated frames for test images with detections.",
    )
    p.add_argument(
        "--output-dir",
        default="outputs/lane_eval/",
        metavar="DIR",
        help="Directory for plots and visualizations.",
    )
    p.add_argument(
        "--labels",
        default="data/mnnit/pseudo_labels/labels.jsonl",
        metavar="JSONL",
        help="Pseudo-label JSONL file for F1 computation.",
    )
    p.add_argument(
        "--log-level",
        default="INFO",
        choices=["DEBUG", "INFO", "WARNING", "ERROR"],
        help="Logging verbosity.",
    )
    return p


def main(argv: Optional[List[str]] = None) -> int:
    """Entry point for the lane evaluation script.

    Parameters
    ----------
    argv:
        Optional argument list (defaults to ``sys.argv``).

    Returns
    -------
    int
        Always ``0`` — this script is diagnostic, not a hard gate.
    """

    parser = _build_parser()
    args = parser.parse_args(argv)

    logging.basicConfig(
        level=getattr(logging, args.log_level),
        format="%(asctime)s  %(levelname)-8s  %(name)s -- %(message)s",
        datefmt="%H:%M:%S",
    )

    # ------------------------------------------------------------------ #
    # 1. Main evaluation                                                   #
    # ------------------------------------------------------------------ #
    stats = evaluate_lane_detector(
        image_dir=args.source,
        config_path=args.config,
        max_images=args.max_images,
        save_visualizations=args.save_viz,
        output_dir=args.output_dir,
    )
    stats.print_report()

    # ------------------------------------------------------------------ #
    # 2. Plots (only when visualizations were requested)                  #
    # ------------------------------------------------------------------ #
    if args.save_viz and stats.total_images > 0:
        print(f"\n{_YELLOW}Generating diagnostic plots ...{_RESET}")

        import yaml
        from app.lane_detection.lane_geometry import LaneGeometryComputer
        from app.lane_detection.bev_transform import BEVConfig, BEVTransform

        with open(args.config, "r") as fh:
            cfg = yaml.safe_load(fh)

        detector = UFLDLaneDetector(config_path=args.config)
        bev = BEVTransform(BEVConfig.from_yaml(args.config))
        geo = LaneGeometryComputer(
            bev_transform=bev, config=cfg["confidence"]
        )

        image_root = Path(args.source)
        all_images = sorted(image_root.glob("rgb_image_*.png"))
        if args.max_images:
            all_images = all_images[: args.max_images]
        split_idx = max(1, int(len(all_images) * 0.9))
        test_images = all_images[split_idx:]

        plot_results: List[LaneEvalResult] = []
        for img_path in tqdm(test_images, desc="Collecting plot data", unit="img"):
            frame = cv2.imread(str(img_path))
            if frame is None:
                continue
            det = detector.predict(frame)
            gm = geo.compute(det)
            plot_results.append(
                LaneEvalResult(
                    image_path=str(img_path),
                    lanes_detected=det.detected_lane_count(min_points=1),
                    left_detected=gm.left_lane_detected,
                    right_detected=gm.right_lane_detected,
                    left_confidence=gm.left_lane_confidence,
                    right_confidence=gm.right_lane_confidence,
                    inference_time_ms=det.inference_time_ms,
                    geometry_valid=gm.lane_geometry_valid,
                    offset_m=gm.offset_m,
                    curvature_inv_m=gm.curvature_inv_m,
                )
            )

        if plot_results:
            plot_evaluation_results(plot_results, output_dir=args.output_dir)

    # ------------------------------------------------------------------ #
    # 3. Pseudo-label F1                                                  #
    # ------------------------------------------------------------------ #
    print()
    f1_result = compute_pseudo_label_f1(
        labels_path=args.labels,
        image_dir=args.source,
        config_path=args.config,
    )

    if "error" in f1_result:
        print(f"{_YELLOW}Pseudo-label F1: {f1_result['error']}{_RESET}")
    else:
        print("Pseudo-label F1 score")
        print(f"  Precision : {f1_result['precision']:.4f}")
        print(f"  Recall    : {f1_result['recall']:.4f}")
        print(f"  F1        : {f1_result['f1']:.4f}")
        print(f"  Support   : {f1_result['support']}")

    return 0


if __name__ == "__main__":
    sys.exit(main())
