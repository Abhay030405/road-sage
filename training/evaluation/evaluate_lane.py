# 4. plot_evaluation_results
# ===========================================================================


def plot_evaluation_results(
    results: List[LaneEvalResult],
    output_dir: str = "outputs/lane_eval/",
) -> None:
    """Create and save a 4-panel diagnostic plot for the evaluation results.

    Panels
    ------
    1. Histogram of left lane confidence scores.
    2. Histogram of right lane confidence scores.
    3. Histogram of inference times.
    4. Scatter plot — lateral offset (x) vs. curvature (y), coloured by
       number of lanes detected.

    The figure is saved to ``output_dir/lane_eval_plots.png``.

    Parameters
    ----------
    results:
        List of :class:`LaneEvalResult` objects from
        :func:`evaluate_lane_detector`.
    output_dir:
        Directory where ``lane_eval_plots.png`` is written.
    """

    out_path = Path(output_dir)
    out_path.mkdir(parents=True, exist_ok=True)

    left_confs = [r.left_confidence for r in results if r.left_detected]
    right_confs = [r.right_confidence for r in results if r.right_detected]
    latencies = [r.inference_time_ms for r in results]
    offsets = [r.offset_m for r in results]
    curvatures = [r.curvature_inv_m for r in results]
    lane_counts = [r.lanes_detected for r in results]

    fig, axes = plt.subplots(2, 2, figsize=(12, 9))
    fig.suptitle(
        "Lane Detector — Evaluation Results", fontsize=14, fontweight="bold"
    )

    # Panel 1 — left confidence
    ax = axes[0, 0]
    ax.hist(left_confs, bins=20, color="steelblue", edgecolor="white")
    ax.axvline(
        _CONFIDENCE_THRESHOLD, color="red", linestyle="--", linewidth=1.2,
        label=f"threshold ({_CONFIDENCE_THRESHOLD})",
    )
    ax.set_title("Left Lane Confidence")
    ax.set_xlabel("Confidence score")
    ax.set_ylabel("Count")
    ax.legend(fontsize=8)

    # Panel 2 — right confidence
    ax = axes[0, 1]
    ax.hist(right_confs, bins=20, color="seagreen", edgecolor="white")
    ax.axvline(
        _CONFIDENCE_THRESHOLD, color="red", linestyle="--", linewidth=1.2,
        label=f"threshold ({_CONFIDENCE_THRESHOLD})",
    )
    ax.set_title("Right Lane Confidence")
    ax.set_xlabel("Confidence score")
    ax.set_ylabel("Count")
    ax.legend(fontsize=8)

    # Panel 3 — inference latency
    ax = axes[1, 0]
    ax.hist(latencies, bins=20, color="darkorange", edgecolor="white")
    ax.axvline(
        _P95_LATENCY_MS, color="red", linestyle="--", linewidth=1.2,
        label=f"P95 target ({_P95_LATENCY_MS:.0f} ms)",
    )
    ax.set_title("Inference Time Distribution")
    ax.set_xlabel("Latency (ms)")
    ax.set_ylabel("Count")
    ax.legend(fontsize=8)

    # Panel 4 — offset vs curvature scatter, coloured by lane count
    ax = axes[1, 1]
    colour_map = {0: "red", 1: "orange", 2: "steelblue"}
    for count in sorted(set(lane_counts)):
        idx = [i for i, r in enumerate(results) if r.lanes_detected == count]
        ox = [offsets[i] for i in idx]
        cy = [curvatures[i] for i in idx]
        ax.scatter(
            ox, cy,
            c=colour_map.get(count, "grey"),
            alpha=0.6, s=18,
            label=f"{count} lane(s)",
        )
    ax.axvline(0, color="grey", linestyle=":", linewidth=0.8)
    ax.axhline(0, color="grey", linestyle=":", linewidth=0.8)
    ax.set_title("Offset vs Curvature")
    ax.set_xlabel("Lateral offset (m)")
    ax.set_ylabel("Curvature (m^-1)")
    ax.legend(fontsize=8)

    plt.tight_layout()

    plot_file = out_path / "lane_eval_plots.png"
    plt.savefig(str(plot_file), dpi=150, bbox_inches="tight")
    plt.close(fig)

    print(f"Plots saved to {plot_file}")


