"""
training.evaluation.evaluate_lane
==================================

Evaluates the UFLD v2 lane detector on held-out MNNIT images.

Since no pixel-level ground truth exists for this dataset, evaluation uses
three proxy measures:
  1. **Lane detection rate** — fraction of test images where both lanes appear.
  2. **Confidence distribution** — histogram of left/right lane scores.
  3. **Per-image visualizations** — annotated frames written to disk.

When pseudo-labels are available (generated by the pseudo-labeller), a
precision / recall / F1 score is also computed.

Usage::

    # Quick run — evaluate on all images, skip visualizations
    python training/evaluation/evaluate_lane.py

    # Evaluate first 100 images and save annotated frames
    python training/evaluation/evaluate_lane.py \\
        --source data/mnnit/rgb \\
        --max-images 100 \\
        --save-viz \\
        --output-dir outputs/lane_eval/

Exit code
---------
``0`` always — this script is diagnostic, not a hard gate.
"""

from __future__ import annotations

import argparse
import json
import logging
import sys
from dataclasses import dataclass
from pathlib import Path
from typing import Dict, List, Optional

import cv2
import matplotlib
matplotlib.use("Agg")  # headless — no display required
import matplotlib.pyplot as plt
import numpy as np
from tqdm import tqdm

# ---------------------------------------------------------------------------
# RoadSage imports
# ---------------------------------------------------------------------------
from app.lane_detection.bev_transform import BEVConfig, BEVTransform
from app.lane_detection.lane_geometry import LaneGeometry, LaneGeometryComputer
from app.lane_detection.ufld_model import LaneDetectionResult, UFLDLaneDetector
from app.explainability.visualizer import VisualizationConfig, create_full_visualization

log = logging.getLogger(__name__)

_GREEN = "\033[92m"
_RED = "\033[91m"
_YELLOW = "\033[93m"
_RESET = "\033[0m"

# ---------------------------------------------------------------------------
# Criteria thresholds
# ---------------------------------------------------------------------------
_DETECTION_RATE_THRESHOLD = 0.40   # >= 40 % of test images must have both lanes
_CONFIDENCE_THRESHOLD = 0.75       # average per-lane confidence
_P95_LATENCY_MS = 100.0            # 95th-percentile inference must be < 100 ms


# ===========================================================================
# 1. LaneEvalResult
# ===========================================================================


@dataclass
class LaneEvalResult:
    """Evaluation result for a single test image.

    Attributes
    ----------
    image_path:
        Absolute path to the source image.
    lanes_detected:
        Total number of lanes returned by the detector (0-4).
    left_detected:
        ``True`` when the left lane marking was found.
    right_detected:
        ``True`` when the right lane marking was found.
    left_confidence:
        Per-lane confidence score in [0, 1] for the left marking.
        0.0 when the lane was not detected.
    right_confidence:
        Per-lane confidence score in [0, 1] for the right marking.
        0.0 when the lane was not detected.
    inference_time_ms:
        Wall-clock time spent inside ``UFLDLaneDetector.predict()`` in
        milliseconds.
    geometry_valid:
        ``True`` when ``LaneGeometryComputer`` produced usable metric output.
    offset_m:
        Signed lateral offset of the vehicle from lane centre in metres.
        Positive means drifted right.
    curvature_inv_m:
        Road curvature in m^-1.  Positive means right curve.
    """

    image_path: str
    lanes_detected: int
    left_detected: bool
    right_detected: bool
    left_confidence: float
    right_confidence: float
    inference_time_ms: float
    geometry_valid: bool
    offset_m: float
    curvature_inv_m: float


# ===========================================================================
# 2. LaneEvalStats
# ===========================================================================


@dataclass
class LaneEvalStats:
    """Aggregate statistics across all evaluated test images.

    Attributes
    ----------
    total_images:
        Number of images in the held-out test split.
    images_with_2_lanes:
        Images where both left *and* right lanes were detected.
    images_with_1_lane:
        Images where exactly one lane was detected.
    images_with_0_lanes:
        Images where no lane was detected.
    detection_rate_2lane:
        Fraction of images with both lanes
        (``images_with_2_lanes / total_images``).
    avg_left_confidence:
        Mean left-lane confidence across images where the left lane was
        detected.  NaN when no left lanes were detected.
    avg_right_confidence:
        Mean right-lane confidence across images where the right lane was
        detected.  NaN when no right lanes were detected.
    avg_inference_ms:
        Mean inference latency in milliseconds.
    p95_inference_ms:
        95th-percentile inference latency in milliseconds.
    avg_offset_m:
        Mean lateral offset across images with valid geometry.
    offset_std_m:
        Standard deviation of lateral offset across images with valid geometry.
    """

    total_images: int
    images_with_2_lanes: int
    images_with_1_lane: int
    images_with_0_lanes: int
    detection_rate_2lane: float
    avg_left_confidence: float
    avg_right_confidence: float
    avg_inference_ms: float
    p95_inference_ms: float
    avg_offset_m: float
    offset_std_m: float

    # ------------------------------------------------------------------
    # Reporting
    # ------------------------------------------------------------------

    def print_report(self) -> None:
        """Print a formatted evaluation report with PASS/FAIL criteria.

        Criteria
        --------
        * Detection rate >= 40 %
        * Average confidence >= 0.75
        * P95 latency < 100 ms
        """

        def _pf(ok: bool) -> str:
            tag = "PASS" if ok else "FAIL"
            colour = _GREEN if ok else _RED
            return f"{colour}{tag}{_RESET}"

        det_ok = self.detection_rate_2lane >= _DETECTION_RATE_THRESHOLD
        # Use the lower of the two means — both must be healthy
        avg_conf = min(self.avg_left_confidence, self.avg_right_confidence)
        conf_ok = avg_conf >= _CONFIDENCE_THRESHOLD
        lat_ok = self.p95_inference_ms < _P95_LATENCY_MS

        divider = "=" * 58
        print(divider)
        print("  Lane Detector — Evaluation Report")
        print(divider)
        print(f"  Test images     : {self.total_images}")
        print(f"  Both lanes      : {self.images_with_2_lanes:>5}  "
              f"({self.detection_rate_2lane * 100:.1f} %)")
        print(f"  One lane        : {self.images_with_1_lane:>5}")
        print(f"  No lanes        : {self.images_with_0_lanes:>5}")
        print()
        print(f"  Avg left conf   : {self.avg_left_confidence:.4f}")
        print(f"  Avg right conf  : {self.avg_right_confidence:.4f}")
        print()
        print(f"  Avg latency     : {self.avg_inference_ms:.1f} ms")
        print(f"  P95 latency     : {self.p95_inference_ms:.1f} ms")
        print()
        print(f"  Avg offset      : {self.avg_offset_m:+.3f} m")
        print(f"  Offset std      : {self.offset_std_m:.3f} m")
        print(divider)
        print(f"  Detection rate >= {_DETECTION_RATE_THRESHOLD*100:.0f}%  "
              f": {_pf(det_ok)}  "
              f"(got {self.detection_rate_2lane*100:.1f}%)")
        print(f"  Avg confidence >= {_CONFIDENCE_THRESHOLD:.2f}  "
              f": {_pf(conf_ok)}  "
              f"(got {avg_conf:.4f})")
        print(f"  P95 latency < {_P95_LATENCY_MS:.0f} ms      "
              f": {_pf(lat_ok)}  "
              f"(got {self.p95_inference_ms:.1f} ms)")
        print(divider)

        all_pass = det_ok and conf_ok and lat_ok
        overall = (f"{_GREEN}ALL PASS{_RESET}" if all_pass
                   else f"{_RED}SOME FAIL{_RESET}")
        print(f"  Overall: {overall}")
        print(divider)


# ===========================================================================
# 3. evaluate_lane_detector
# ===========================================================================


def evaluate_lane_detector(
    image_dir: str = "rgb",
    config_path: str = "configs/lane_detection.yaml",
    max_images: Optional[int] = None,
    save_visualizations: bool = False,
    output_dir: str = "outputs/lane_eval/",
) -> LaneEvalStats:
    """Evaluate the UFLD v2 lane detector on held-out MNNIT images.

    Applies a deterministic 90/10 train/test split by index (last 10 % of
    sorted filenames become the held-out set).  No pixel-level ground truth
    is required — quality is assessed via proxy metrics.

    Parameters
    ----------
    image_dir:
        Directory containing ``rgb_image_*.png`` frames.
    config_path:
        Path to ``lane_detection.yaml``.
    max_images:
        If given, cap the total number of images considered *before* the
        train/test split.  Useful for quick smoke-tests.
    save_visualizations:
        When ``True``, write an annotated PNG for every test image that has
        at least one detected lane.
    output_dir:
        Directory where visualizations and plots are written.

    Returns
    -------
    LaneEvalStats
        Aggregate metrics computed over the held-out test split.
    """

    import yaml

    log.info("Loading configuration from %s", config_path)
    with open(config_path, "r") as fh:
        cfg = yaml.safe_load(fh)

    # ------------------------------------------------------------------ #
    # Initialise components                                                #
    # ------------------------------------------------------------------ #
    log.info("Initialising UFLDLaneDetector ...")
    detector = UFLDLaneDetector(config_path=config_path)

    log.info("Initialising BEVTransform ...")
    bev_cfg = BEVConfig.from_yaml(config_path)
    bev = BEVTransform(bev_cfg)

    log.info("Initialising LaneGeometryComputer ...")
    geometry_computer = LaneGeometryComputer(
        bev_transform=bev, config=cfg["confidence"]
    )

    vis_cfg: Optional[VisualizationConfig] = None
    if save_visualizations:
        vis_cfg = VisualizationConfig.from_yaml(config_path)

    # ------------------------------------------------------------------ #
    # Discover images                                                      #
    # ------------------------------------------------------------------ #
    image_root = Path(image_dir)
    all_images = sorted(image_root.glob("rgb_image_*.png"))

    if not all_images:
        log.warning(
            "No images found in %s matching rgb_image_*.png", image_dir
        )
        return _empty_stats()

    if max_images is not None:
        all_images = all_images[:max_images]

    # 90 / 10 split — last 10 % is the held-out test set
    split_idx = max(1, int(len(all_images) * 0.9))
    test_images = all_images[split_idx:]

    log.info(
        "Total: %d  |  Train: %d  |  Test: %d",
        len(all_images),
        split_idx,
        len(test_images),
    )

    if not test_images:
        log.warning("Test split is empty — consider using more images.")
        return _empty_stats()

    # ------------------------------------------------------------------ #
    # Output directory                                                     #
    # ------------------------------------------------------------------ #
    out_path = Path(output_dir)
    if save_visualizations:
        out_path.mkdir(parents=True, exist_ok=True)

    # ------------------------------------------------------------------ #
    # Per-image evaluation                                                 #
    # ------------------------------------------------------------------ #
    results: List[LaneEvalResult] = []

    for img_path in tqdm(test_images, desc="Evaluating", unit="img"):
        frame = cv2.imread(str(img_path))
        if frame is None:
            log.warning("Could not read %s — skipping.", img_path)
            continue

        # ---- detection -----------------------------------------------
        detection: LaneDetectionResult = detector.predict(frame)

        # ---- geometry ------------------------------------------------
        geometry: LaneGeometry = geometry_computer.compute(detection)

        # ---- build result --------------------------------------------
        lane_count = detection.detected_lane_count(min_points=1)
        result = LaneEvalResult(
            image_path=str(img_path),
            lanes_detected=lane_count,
            left_detected=geometry.left_lane_detected,
            right_detected=geometry.right_lane_detected,
            left_confidence=geometry.left_lane_confidence,
            right_confidence=geometry.right_lane_confidence,
            inference_time_ms=detection.inference_time_ms,
            geometry_valid=geometry.lane_geometry_valid,
            offset_m=geometry.offset_m,
            curvature_inv_m=geometry.curvature_inv_m,
        )
        results.append(result)

        # ---- optional visualization ----------------------------------
        if save_visualizations and lane_count > 0:
            try:
                bev_image = bev.transform_image(frame)
                vis = create_full_visualization(
                    original_image=frame,
                    detection=detection,
                    geometry=geometry,
                    bev_image=bev_image,
                    config=vis_cfg,
                )
                out_file = out_path / f"eval_{img_path.name}"
                cv2.imwrite(str(out_file), vis)
            except Exception as exc:
                log.warning(
                    "Visualization failed for %s: %s", img_path.name, exc
                )

    return _compute_stats(results)


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _empty_stats() -> LaneEvalStats:
    """Return a zeroed-out LaneEvalStats when no images are available."""
    return LaneEvalStats(
        total_images=0,
        images_with_2_lanes=0,
        images_with_1_lane=0,
        images_with_0_lanes=0,
        detection_rate_2lane=0.0,
        avg_left_confidence=float("nan"),
        avg_right_confidence=float("nan"),
        avg_inference_ms=0.0,
        p95_inference_ms=0.0,
        avg_offset_m=0.0,
        offset_std_m=0.0,
    )


def _compute_stats(results: List[LaneEvalResult]) -> LaneEvalStats:
    """Compute aggregate LaneEvalStats from a list of per-image results."""
    n = len(results)
    if n == 0:
        log.error("No results collected — returning empty stats.")
        return _empty_stats()

    n2 = sum(1 for r in results if r.left_detected and r.right_detected)
    n1 = sum(1 for r in results if r.lanes_detected == 1)
    n0 = sum(1 for r in results if r.lanes_detected == 0)

    left_confs = [r.left_confidence for r in results if r.left_detected]
    right_confs = [r.right_confidence for r in results if r.right_detected]
    latencies = [r.inference_time_ms for r in results]
    offsets = [r.offset_m for r in results if r.geometry_valid]

    return LaneEvalStats(
        total_images=n,
        images_with_2_lanes=n2,
        images_with_1_lane=n1,
        images_with_0_lanes=n0,
        detection_rate_2lane=n2 / n,
        avg_left_confidence=(
            float(np.mean(left_confs)) if left_confs else float("nan")
        ),
        avg_right_confidence=(
            float(np.mean(right_confs)) if right_confs else float("nan")
        ),
        avg_inference_ms=float(np.mean(latencies)),
        p95_inference_ms=float(np.percentile(latencies, 95)),
        avg_offset_m=float(np.mean(offsets)) if offsets else 0.0,
        offset_std_m=float(np.std(offsets)) if offsets else 0.0,
    )


