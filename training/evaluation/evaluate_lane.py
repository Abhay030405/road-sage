# 5. compute_pseudo_label_f1
# ===========================================================================


def compute_pseudo_label_f1(
    labels_path: str = "data/mnnit/pseudo_labels/labels.jsonl",
    image_dir: str = "rgb",
    config_path: str = "configs/lane_detection.yaml",
) -> Dict[str, object]:
    """Compute precision, recall, and F1 against available pseudo-labels.

    A pseudo-label record is treated as a positive ground-truth annotation
    (i.e., the labeller judged that lanes were visible in that frame).

    Classification scheme
    ---------------------
    * **TP** — pseudo-label exists *and* detector found at least one lane.
    * **FN** — pseudo-label exists *but* detector found no lanes.
    * **FP** — no pseudo-label *but* detector found at least one lane.
    * **TN** — no pseudo-label *and* no detection (ignored).

    Parameters
    ----------
    labels_path:
        Path to the JSONL file produced by the pseudo-labeller.  Each line
        must be a JSON object with at least an ``"image"`` key holding the
        image filename (basename).
    image_dir:
        Directory containing ``rgb_image_*.png`` frames.
    config_path:
        Path to ``lane_detection.yaml``.

    Returns
    -------
    dict
        ``{"precision": float, "recall": float, "f1": float, "support": int}``
        on success, or ``{"error": "no pseudo-labels found"}`` when the
        labels file is absent or empty.
    """

    labels_file = Path(labels_path)
    if not labels_file.exists():
        log.warning("Pseudo-label file not found: %s", labels_path)
        return {"error": "no pseudo-labels found"}

    # ---- load labels ------------------------------------------------
    labeled: set = set()
    with open(labels_file, "r") as fh:
        for line in fh:
            line = line.strip()
            if not line:
                continue
            try:
                rec = json.loads(line)
                img_name = Path(rec.get("image", "")).name
                if img_name:
                    labeled.add(img_name)
            except json.JSONDecodeError as exc:
                log.warning("Skipping malformed label line: %s", exc)

    if not labeled:
        log.warning("Pseudo-label file exists but contains no valid records.")
        return {"error": "no pseudo-labels found"}

    log.info("Loaded %d pseudo-label records.", len(labeled))

    # ---- init detector -----------------------------------------------
    detector = UFLDLaneDetector(config_path=config_path)
    image_root = Path(image_dir)

    tp = fn = fp = 0

    all_images = sorted(image_root.glob("rgb_image_*.png"))
    for img_path in tqdm(all_images, desc="F1 eval", unit="img"):
        fname = img_path.name
        has_label = fname in labeled

        frame = cv2.imread(str(img_path))
        if frame is None:
            continue

        detection = detector.predict(frame)
        detected = detection.detected_lane_count(min_points=1) > 0

        if has_label and detected:
            tp += 1
        elif has_label and not detected:
            fn += 1
        elif not has_label and detected:
            fp += 1
        # not has_label and not detected -> TN, ignored

    support = tp + fn
    if support == 0:
        return {"error": "no pseudo-labels found"}

    precision = tp / (tp + fp) if (tp + fp) > 0 else 0.0
    recall = tp / (tp + fn) if (tp + fn) > 0 else 0.0
    f1 = (
        2 * precision * recall / (precision + recall)
        if (precision + recall) > 0
        else 0.0
    )

    log.info(
        "Pseudo-label F1 — P=%.3f  R=%.3f  F1=%.3f  support=%d",
        precision, recall, f1, support,
    )

    return {
        "precision": round(precision, 4),
        "recall": round(recall, 4),
        "f1": round(f1, 4),
        "support": support,
    }


